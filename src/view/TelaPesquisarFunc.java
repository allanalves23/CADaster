    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int counter=0;
        if(campoNome.getText().length()==0){
            if(radioBtnTodos.isSelected()){
                counter=1;
            }else if(radioBtnProf.isSelected()){
                counter=2;
            }else if(radioBtnDiretor.isSelected()){
                counter=3;
            }else if(radioBtnAdm.isSelected()){
                counter=4;
            }else if(radioBtnOutros.isSelected()){
                counter=5;
            }else if(radioBtnSer.isSelected()){
                counter=6;
            }
            DefaultTableModel model = (DefaultTableModel) tabelaFuncionario.getModel();
            model.setRowCount(0);
            SearchDao sd = new SearchDao();
            for(EmployeeBean employee : sd.procurarEmployee(counter)){
                model.addRow(new Object[]{
                employee.getRegistro(),
                employee.getNome(),
                employee.getSexo(),
                employee.getDataNascimento(),
                employee.getCPF(),
                employee.getCargo(),
                employee.getSalario(),
                employee.getCEP(),
                employee.getEndereco()
            });
        }
        }else{
            if(radioBtnTodos.isSelected()){
                counter=1;
            }else if(radioBtnProf.isSelected()){
                counter=2;
            }else if(radioBtnDiretor.isSelected()){
                counter=3;
            }else if(radioBtnAdm.isSelected()){
                counter=4;
            }else if(radioBtnOutros.isSelected()){
                counter=5;
            }else if(radioBtnSer.isSelected()){
                counter=6;
            }
            DefaultTableModel model = (DefaultTableModel) tabelaFuncionario.getModel();
            model.setRowCount(0);
            SearchDao sd = new SearchDao();
            for(EmployeeBean employee : sd.procurarEmployee(counter,campoNome.getText(),true)){
                model.addRow(new Object[]{
                employee.getRegistro(),
                employee.getNome(),
                employee.getSexo(),
                employee.getDataNascimento(),
                employee.getCPF(),
                employee.getCargo(),
                employee.getSalario(),
                employee.getCEP(),
                employee.getEndereco()
            });
        }
        }
        
        if(tabelaFuncionario.getRowCount()==0){
            lblInfo.setText("Nennhum registro encontrado");
            lblInfo.setIcon(new ImageIcon(getClass().getResource("../imagens/warningSmall.png")));
        }else{
            lblInfo.setText("");
            lblInfo.setIcon(new ImageIcon(getClass().getResource("")));
        }
        
        if(lblInfo2.getText().length()!=0){
           lblInfo2.setText("");
           lblInfo2.setIcon(new ImageIcon(getClass().getResource("")));
           
        }
        profissoes.clearSelection();
        campoNome.setText("");
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnLimparTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparTabelaActionPerformed
       if(tabelaFuncionario.getRowCount()==0){
           lblInfo2.setText("Tabela já está limpa!");
           lblInfo2.setIcon(new ImageIcon(getClass().getResource("../imagens/warningSmall.png")));
       }else{
           DefaultTableModel model = (DefaultTableModel) tabelaFuncionario.getModel();
           model.setNumRows(0);
           lblInfo2.setText("");
           lblInfo2.setIcon(new ImageIcon(getClass().getResource("")));
           
       }
    }//GEN-LAST:event_btnLimparTabelaActionPerformed


